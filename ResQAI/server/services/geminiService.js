/**
 * Analyze symptoms text with Gemini
 */
const analyzeSymptomsText = async (data) => {
  const { age, gender, symptoms, painLevel, duration, medicalHistory, allergies, medications } = data;

  if (isDummyKey) {
    console.warn('ResQAI Warning: Using simulated triage backend. Please set GEMINI_API_KEY in .env for live AI.');
    return getSimulatedTriage(age, gender, symptoms, painLevel, duration);
  }

  try {
    const model = genAI.getGenerativeModel({
      model: 'gemini-1.5-flash',
      generationConfig: {
        responseMimeType: 'application/json',
      },
    });

    const prompt = `
      ${SYSTEM_INSTRUCTIONS}
      
      Patient Case for Triage:
      - Age: ${age}
      - Gender: ${gender}
      - Chief Complaint Symptoms: ${symptoms}
      - Pain Level (1-10): ${painLevel}
      - Duration: ${duration}
      - Medical History: ${medicalHistory || 'None reported'}
      - Known Allergies: ${allergies || 'None reported'}
      - Current Medications: ${medications || 'None reported'}

      Please return a JSON response matching this EXACT schema:
      {
        "severity": "Low" | "Moderate" | "Critical",
        "confidence": number (float between 0 and 1),
        "possible_conditions": ["condition 1", "condition 2", ...],
        "reasoning": "detailed explanation of why this risk level was chosen",
        "first_aid": ["immediate instruction 1", "instruction 2", ...],
        "next_steps": ["action 1", "action 2", ...],
        "warning_signs": ["red flag 1", "red flag 2", ...],
        "hospital_needed": boolean,
        "ambulance_required": boolean,
        "estimated_priority": "Immediate (Level 1)" | "Emergent (Level 2)" | "Urgent (Level 3)" | "Semi-urgent (Level 4)" | "Non-urgent (Level 5)",
        "disclaimer": "standard medical liability disclaimer"
      }
    `;

    const result = await model.generateContent(prompt);
    const text = result.response.text();
    return JSON.parse(text);
  } catch (error) {
    console.error('Gemini API call failed, falling back to simulation:', error.message);
    return getSimulatedTriage(age, gender, symptoms, painLevel, duration);
  }
};

/**
 * Analyze an uploaded wound/burn image along with text descriptions
 */
const analyzeWoundImage = async (imageBuffer, mimeType, description = '') => {
  if (isDummyKey) {
    console.warn('ResQAI Warning: Using simulated image triage. Please set GEMINI_API_KEY in .env for live AI.');
    const result = getSimulatedImageTriage(mimeType);
    if (description) {
      result.reasoning += ` User description: "${description}" was incorporated in this assessment.`;
    }
    return result;
  }

  try {
    const model = genAI.getGenerativeModel({
      model: 'gemini-1.5-flash',
      generationConfig: {
        responseMimeType: 'application/json',
      },
    });

    const imagePart = bufferToGenerativePart(imageBuffer, mimeType);
    const prompt = `
      ${SYSTEM_INSTRUCTIONS}
      
      You are analyzing an image of a wound, burn, or injury. 
      ${description ? `The user provided the following context: "${description}"` : ''}

      Perform visual emergency triage. Identify the severity of the injury, possible type (e.g. burn, abrasion, laceration, infection, etc.), first-aid steps, next actions, and warning signs.
      
      Return a JSON response matching this EXACT schema:
      {
        "severity": "Low" | "Moderate" | "Critical",
        "confidence": number (float between 0 and 1),
        "possible_conditions": ["condition 1", "condition 2", ...],
        "reasoning": "detailed explanation of what you see visually and what it implies",
        "first_aid": ["immediate instruction 1", "instruction 2", ...],
        "next_steps": ["action 1", "action 2", ...],
        "warning_signs": ["red flag 1", "red flag 2", ...],
        "hospital_needed": boolean,
        "ambulance_required": boolean,
        "estimated_priority": "Immediate (Level 1)" | "Emergent (Level 2)" | "Urgent (Level 3)" | "Semi-urgent (Level 4)" | "Non-urgent (Level 5)",
        "disclaimer": "standard visual medical liability disclaimer"
      }
    `;

    const result = await model.generateContent([prompt, imagePart]);
    const text = result.response.text();
    return JSON.parse(text);
  } catch (error) {
    console.error('Gemini Image API call failed, falling back to simulation:', error.message);
    const result = getSimulatedImageTriage(mimeType);
    if (description) {
      result.reasoning += ` User description: "${description}" was incorporated in this fallback assessment.`;
    }
    return result;
  }
};

/**
 * Disaster advice generation helper
 */
const getDisasterAdvice = async (category, situation = '') => {
  if (isDummyKey) {
    // Return standard guidance based on category
    return getOfflineDisasterGuide(category);
  }

  try {
    const model = genAI.getGenerativeModel({
      model: 'gemini-1.5-flash',
      generationConfig: {
        responseMimeType: 'application/json',
      },
    });

    const prompt = `
      You are an expert disaster response coordination AI.
      Provide response instructions for the disaster category: "${category}".
      User's specific situation/question: "${situation || 'None'}"

      Return a JSON response matching this EXACT schema:
      {
        "immediate_actions": ["action 1", "action 2", ...],
        "dos": ["do 1", "do 2", ...],
        "donts": ["dont 1", "dont 2", ...],
        "evacuation_advice": ["advice 1", "advice 2", ...]
      }
    `;

    const result = await model.generateContent(prompt);
    const text = result.response.text();
    return JSON.parse(text);
  } catch (error) {
    console.error('Gemini Disaster advice failed, falling back to cached guide:', error.message);
    return getOfflineDisasterGuide(category);
  }
};

