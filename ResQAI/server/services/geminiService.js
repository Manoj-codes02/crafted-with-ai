const { GoogleGenerativeAI } = require('@google/generative-ai');

// Initialize the Gemini API client
const apiKey = process.env.GEMINI_API_KEY;
const isDummyKey = !apiKey || apiKey === 'YOUR_GEMINI_API_KEY' || apiKey.trim() === '';

let genAI;
if (!isDummyKey) {
  genAI = new GoogleGenerativeAI(apiKey);
}

// System instructions for the model
const SYSTEM_INSTRUCTIONS = `
You are an advanced AI Emergency Triage & Disaster Response Assistant named ResQAI. 
Your role is to assess symptoms, estimate risk severity, provide immediate first-aid guidance, and offer response instructions.
CRITICAL RULES:
1. NEVER definitively diagnose a patient. You are a triage assistant, not a doctor.
2. Use clear, cautious medical language.
3. Always include appropriate medical disclaimers.
4. Classify risk level strictly as: Low, Moderate, or Critical.
5. In case of critical danger, always recommend immediate emergency service contact (911/112/108, etc.) and hospital transit.
6. Return your analysis in valid, parseable JSON only. No markdown formatting around the JSON.
`;

/**
 * Convert a Buffer to a generative part object for Gemini
 */
const bufferToGenerativePart = (buffer, mimeType) => {
  return {
    inlineData: {
      data: buffer.toString('base64'),
      mimeType,
    },
  };
};

/**
 * Triage simulation fallback in case Gemini API is not configured or fails
 */
const getSimulatedTriage = (age, gender, symptoms, painLevel, duration) => {
  const symLower = symptoms.toLowerCase();
  
  let severity = 'Low';
  let confidence = 0.85;
  let possible_conditions = ['Mild Muscle Strain', 'General Fatigue'];
  let reasoning = 'The symptoms described do not match immediate high-risk cardiovascular or neurological emergencies. Symptoms appear localized and non-spreading.';
  let first_aid = [
    'Rest the affected area and avoid strenuous movement.',
    'Keep hydrated and monitor body temperature.',
    'Over-the-counter pain relievers can be considered if appropriate.'
  ];
  let next_steps = [
    'Monitor symptoms over the next 24-48 hours.',
    'Schedule a non-urgent consult with a primary care physician if symptoms persist.'
  ];
  let warning_signs = [
    'Sudden onset of severe shortness of breath.',
    'Chest pain radiating to the arm, neck, or jaw.',
    'Loss of speech, balance, or sudden weakness on one side of the body.'
  ];
  let hospital_needed = false;
  let ambulance_required = false;
  let estimated_priority = 'Non-urgent (Level 5)';
  const disclaimer = 'ResQAI triage reports are generated by AI for informational purposes only. This is not a medical diagnosis. If you feel you are in immediate danger, call emergency services immediately.';

  // Chest pain / Heart attack risk
  if (symLower.includes('chest') || symLower.includes('heart') || symLower.includes('breathing') || symLower.includes('shortness of breath') || painLevel >= 8) {
    severity = 'Critical';
    confidence = 0.92;
    possible_conditions = ['Acute Coronary Syndrome', 'Severe Respiratory Distress', 'Angina Pectoris'];
    reasoning = 'Chest pain and shortness of breath combined with a high pain level are classic markers of cardiac or respiratory emergencies. Immediate medical assessment is mandatory.';
    first_aid = [
      'Stop all physical activity and sit in a comfortable, upright position.',
      'Loosen tight clothing around the neck and waist.',
      'If prescribed, take emergency heart medication (e.g. nitroglycerin).',
      'If aspirin is available and the patient has no allergy or contraindications, chew a standard adult aspirin (325mg).'
    ];
    next_steps = [
      'Call local emergency services (e.g., 911 / 112 / 108) immediately.',
      'Do not attempt to drive yourself to the hospital.',
      'Prepare a list of medications and allergies for emergency staff.'
    ];
    warning_signs = [
      'Loss of consciousness or fainting.',
      'Bluish lips or fingertips (cyanosis).',
      'Profuse cold sweating, nausea, or dizziness.'
    ];
    hospital_needed = true;
    ambulance_required = true;
    estimated_priority = 'Immediate (Level 1)';
  } 
  // Moderate pain / minor injury / moderate symptoms
  else if (symLower.includes('burn') || symLower.includes('wound') || symLower.includes('cut') || symLower.includes('fracture') || symLower.includes('fever') || painLevel >= 5) {
    severity = 'Moderate';
    confidence = 0.88;
    possible_conditions = ['Second-Degree Burn', 'Laceration with Infection Risk', 'Suspected Fracture/Sprain'];
    reasoning = 'Symptoms indicate a moderate-level localized trauma or inflammatory response. While not immediately life-threatening, it requires prompt professional treatment to prevent complications.';
    first_aid = [
      'Clean the area gently with sterile water if it is an open wound. Avoid chemical disinfectants like alcohol directly on raw tissue.',
      'Apply light pressure with a clean cloth to stop bleeding.',
      'Elevate the affected limb if possible.',
      'Apply a cool compress for burns (do not use ice directly).'
    ];
    next_steps = [
      'Visit an urgent care clinic or outpatient department within 12 hours.',
      'Ensure tetanus vaccination is up to date (within last 5-10 years).'
    ];
    warning_signs = [
      'Red streaks spreading from the wound.',
      'Uncontrolled bleeding that does not stop after 10 minutes of direct pressure.',
      'Spiking fever above 102°F (38.9°C) or chills.'
    ];
    hospital_needed = true;
    ambulance_required = false;
    estimated_priority = 'Urgent (Level 3)';
  }

  return {
    severity,
    confidence,
    possible_conditions,
    reasoning,
    first_aid,
    next_steps,
    warning_signs,
    hospital_needed,
    ambulance_required,
    estimated_priority,
    disclaimer
  };
};

